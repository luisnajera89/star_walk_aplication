/// Functions to query StarshipRecords (as a Stream and as a Future).
Stream<List<StarshipRecord>> queryStarshipRecord({
  Query Function(Query)? queryBuilder,
  int limit = -1,
  bool singleRecord = false,
}) =>
    queryCollection(
      StarshipRecord.collection,
      StarshipRecord.serializer,
      queryBuilder: queryBuilder,
      limit: limit,
      singleRecord: singleRecord,
    );

